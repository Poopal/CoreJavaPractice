package JavaSessions;

public class A {
	
	public A()
	{
		System.out.println(" This is Parent Class Constructor");
	}
	
	public A(int i )
	{
		System.out.println(" This is Parent Class Constructor with single parameter : " +  i );
	}

	public A(int i, int j )
	{
		System.out.println(" This is Parent Class Constructor with two Parameter : " +  i );
		System.out.println(" This is Parent Class Constructor with two Parameter : " +  j );
	}
	

}
